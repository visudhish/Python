#This program displays numbers in Descending order
numbers = range(10,0,-1)
for num in numbers:
    print(num)
#end    
#This program displays serial numbers from 1-10
numbers = range(1,11)
for num in numbers:
    print(num)
#end    
#This program displays odd number
numbers = range(1,10,2)
for num in numbers:
    print(num) 
#end 
#This program displays even number
numbers = range(0,10,2)
for num in numbers:
    print(num)       
#end    